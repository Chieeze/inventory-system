    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int num=Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/archie","root","");
            Statement st = con.createStatement();
            ResultSet res = st.executeQuery("select * from archie where Item="+num);
            
            while(res.next()){
                category_cb.setSelectedItem(res.getString("Category"));
                txtproduct.setText(res.getString("Product"));
                txtquantity.setText(res.getString("Quantity"));
                txtprice.setText(res.getString("Price"));
                txtexp.setText(res.getString("Expiration"));
                amountshow.setText(res.getString("Amount"));
            }
        }catch(Exception e){
            System.err.println(e.getMessage());
        }
    }//GEN-LAST:event_jTable1MouseClicked

